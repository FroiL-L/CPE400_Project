class Coords:
    def __init__(self, x: int = 0,
                 y: int = 0,
                 z: int = 0) -> None:
        self.x = x
        self.y = y
        self.z = z
        
    ###########################################
    # getTupe():
    #   Get the coordinates in a list.
    # args:
    #   NA.
    # return:
    #   List.
    ###########################################
    def getList(self):
        return [self.x, self.y, self.z]
